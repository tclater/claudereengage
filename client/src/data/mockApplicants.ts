import type { Applicant, JobBoard } from '../types/candidate';

const today = new Date();
const daysAgo = (n: number) => {
  const d = new Date(today);
  d.setDate(d.getDate() - n);
  return d.toISOString().split('T')[0];
};

// Source → candidateType mapping (used throughout the app)
export const ACTIVE_BOARDS: JobBoard[] = ['LinkedIn', 'Indeed'];
